# ==== Integer =====
# description: Integers are whole numbers

# + Add two numbers together
1 + 2  # => 3

# - Subtract second from first
2 - 1  # => 1

# < First less than second
3 < 5  # => true
5 < 3  # => false
3 < 3  # => false

# == Equality test
3 == 3  # => true
3 == 4  # => false

# > First greater than second
5 > 3  # => true
3 > 5  # => false
3 > 3  # => false

# <= First less than or equal to second
4 <= 4 # true
3 <= 4 # true
4 <= 3 # false

# >= First greater than or equal to second
6 >= 5 # true
6 >= 6 # true
6 >= 9 # false

# to_f Change integer to float
9.to_f # 9.0

# even Returns true is integer is even, false otherwise
5.even? # false
6.even? # true
