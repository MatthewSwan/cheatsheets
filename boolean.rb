# ===== Booleans =====
# description:

# && Either both true or false
5 > 3 && 2 < 8

# || Either both false or true
6 == 7 || true

# if If statement resulting in 'true' or 'false' answer
if (3 > 2) # true
if (2 > 3) # false

# unless
x = 1
unless (x > 3)
  puts "x < 3" #true
else
  puts "x > 3"
end

